<script>
	let options = [
		{ label: 'Home', path: '/' },
		{ label: 'Blog List', path: '/blog' },
		{ label: 'Blog Sample', path: '/blog/1' }
	];
	let selectedOption = options[0];
	let isOpen = false;

	function toggleDropdown() {
		isOpen = !isOpen;
	}

	function selectOption(option) {
		selectedOption = option;
		isOpen = false;
	}
</script>

<div class="relative inline-block text-left">
	<div>
		<button
			type="button"
			class="inline-flex justify-center w-full rounded-md border border-gray-300 shadow-sm px-4 bg-white text-sm font-medium text-gray-700 hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500"
			id="options-menu"
			aria-haspopup="true"
			aria-expanded={isOpen}
			on:click={toggleDropdown}
		>
			{selectedOption.label}
			<svg
				class="-mr-1 ml-2 h-5 w-5"
				xmlns="http://www.w3.org/2000/svg"
				viewBox="0 0 20 20"
				fill="currentColor"
				aria-hidden="true"
			>
				<path
					fill-rule="evenodd"
					d="M6.293 7.293a1 1 0 011.414 0L10 9.586l2.293-2.293a1 1 0 111.414 1.414l-3 3a1 1 0 01-1.414 0l-3-3a1 1 0 010-1.414z"
					clip-rule="evenodd"
				/>
			</svg>
		</button>
	</div>
	{#if isOpen}
		<div
			class="origin-top-right absolute right-0 mt-2 w-56 rounded-md shadow-lg bg-white ring-1 ring-black ring-opacity-5"
			role="menu"
			aria-orientation="vertical"
			aria-labelledby="options-menu"
		>
			<div class="py-1" role="none">
				{#each options as option (option)}
					<a
						href={option.path}
						rel="self"
						class="block px-4 py-2 text-sm text-gray-700 hover:bg-gray-100 hover:text-gray-900"
						role="menuitem"
						on:click={() => selectOption(option.label)}
					>
						{option.label}
					</a>
				{/each}
			</div>
		</div>
	{/if}
</div>
